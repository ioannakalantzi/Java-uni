public class Consoles extends GamingDevice {
    public Consoles(int quantity, String code, String modelName, int year, String manufacturer, double price, String type, String processor, String graphics, String sound, int storageCapacity){
        this.quantity = quantity;
        this.code = code;
        this.modelName = modelName;
        this.year = year;
        this.manufacturer = manufacturer;
        this.price = price;
        this.type = type;
        this.processor = processor;
        this.graphics = graphics;
        this.sound = sound;
        this.storageCapacity = storageCapacity;
    }
}